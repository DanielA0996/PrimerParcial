import java.util.Scanner;

/**
 *
 * @author fermi24
 */
public class Primos {

    int contador = 0;

    public boolean esPrimo(int numero) {
        int contador = 2;
        boolean primo = true;
        while ((primo) && (contador != numero)) {
            if (numero % contador == 0) {
                primo = false;
            }
            contador++;
        }
        return primo;
    }

}
